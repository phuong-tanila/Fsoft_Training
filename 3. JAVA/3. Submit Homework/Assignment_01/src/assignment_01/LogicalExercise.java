public class LogicalExercise {

    public static void main(String[] args) {
        int a = 25;
        int b = 39;

        if (a != b) {
            System.out.printf("%d != %d\n", a, b);
        }
        if (a < b) {
            System.out.printf("%d < %d\n", a, b);
        }
        System.out.printf("%d <= %d\n", a, b);
    }
}
