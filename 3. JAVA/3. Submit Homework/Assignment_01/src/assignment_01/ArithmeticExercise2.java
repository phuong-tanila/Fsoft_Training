public class ArithmeticExercise2 {

    public static void main(String[] args) {
        int a = 125;
        int b = 24;

        System.out.printf("%d + %d = %d\n", a, b, (a + b));
        System.out.printf("%d - %d = %d\n", a, b, (a - b));
        System.out.printf("%d * %d = %d\n", a, b, (a * b));
        System.out.printf("%d / %d = %d\n", a, b, (a / b));
        System.out.printf("%d %% %d = %d\n", a, b, (a % b));
    }

}
